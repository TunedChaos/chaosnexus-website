// docs/.vitepress/scripts/generate-api-docs.mjs
//
// Reads the committed SSOT schema (`docs/.vitepress/chaos_schema.json`) and
// auto-generates VitePress markdown pages for the Rhai engine API under
// `docs/api/rhai/index.md`.
//
// It injects an <ApiFilter /> component at the top to allow instantly 
// filtering the massive list of functions on the client-side.
//
// Runs automatically before `website:dev` / `website:build` (see package.json). 

import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const vitepressDir = resolve(scriptDir, "..");
const docsRoot = resolve(scriptDir, "../..");
const schemaPath = resolve(vitepressDir, "chaos_schema.json");
const apiRoot = join(docsRoot, "api", "rhai");

// Marker used to identify (and safely clean) auto-generated pages.
const MARKER = "<!-- AUTO-GENERATED by generate-api-docs.mjs - do not edit by hand -->";

/** Escapes a string for safe inclusion inside a markdown table cell. */
function escapeCell(value) {
  return String(value ?? "")
    .replace(/\|/g, "\\|")
    .replace(/\r?\n/g, " ")
    .trim();
}

/** Removes previously generated module directories (those carrying MARKER). */
function cleanGeneratedDirs() {
  if (!existsSync(apiRoot)) return;
  for (const entry of readdirSync(apiRoot, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const indexPath = join(apiRoot, entry.name, "index.md");
    if (existsSync(indexPath) && readFileSync(indexPath, "utf-8").startsWith(MARKER)) {
      rmSync(join(apiRoot, entry.name), { recursive: true, force: true });
    }
  }
}

function main() {
  mkdirSync(apiRoot, { recursive: true });

  if (!existsSync(schemaPath)) {
    console.warn(
      `[generate-api-docs] No schema found at ${schemaPath}. Run \`pnpm schema:generate\` first. Skipping API page generation.`
    );
    return;
  }

  const schema = JSON.parse(readFileSync(schemaPath, "utf-8"));
  const modules = schema.modules ?? {};

  cleanGeneratedDirs();

  const lines = [];
  lines.push("---");
  lines.push("title: Rhai API Reference");
  lines.push("outline: false");
  lines.push("---");
  lines.push(MARKER);
  lines.push("");
  lines.push("<script setup>");
  lines.push("import ApiFilter from '../../.vitepress/components/ApiFilter.vue'");
  lines.push("</script>");
  lines.push("");
  lines.push("# Rhai API Reference");
  lines.push("");
  lines.push("Auto-generated from the ChaosNexus Anvil engine schema. The engine is the single source of truth: every function below is emitted by the compiled runtime.");
  lines.push("");
  if (schema.meta?.version) {
    lines.push(`_Engine version: \`${schema.meta.version}\`${schema.meta.generated_at ? ` - generated ${schema.meta.generated_at}` : ""}._`);
    lines.push("");
  }
  
  lines.push("<ApiFilter />");
  lines.push("");

  let fnTotal = 0;
  const sidebarModules = [];
  
  for (const [moduleName, mod] of Object.entries(modules)) {
    const sidebarItems = [];
    
    lines.push(`<div class="api-module" data-name="${moduleName}">`);
    lines.push("");
    lines.push(`## \`${moduleName}\` module`);
    lines.push("");
    if (mod.description) {
      lines.push(mod.description);
      lines.push("");
    }

    const functions = Array.isArray(mod.functions) ? mod.functions : [];
    for (const fn of functions) {
      fnTotal++;
      lines.push(`<div class="api-function" data-name="${fn.name}" data-module="${moduleName}">`);
      lines.push("");
      lines.push(`### \`${fn.name}\``);
      lines.push("");
      if (fn.description) {
        lines.push(fn.description);
        lines.push("");
      }
      lines.push("#### Signature");
      lines.push("");
      lines.push("```rust");
      lines.push(fn.signature || `${fn.name}(...)`);
      lines.push("```");
      lines.push("");
      
      const params = Array.isArray(fn.parameters) ? fn.parameters : [];
      if (params.length > 0) {
        lines.push("#### Parameters");
        lines.push("");
        lines.push("| Name | Type | Description |");
        lines.push("| ---- | ---- | ----------- |");
        for (const p of params) {
          lines.push(
            `| \`${escapeCell(p.name)}\` | \`${escapeCell(p.type)}\` | ${escapeCell(p.description) || "-"} |`
          );
        }
        lines.push("");
      }

      lines.push("#### Returns");
      lines.push(`\`${fn.return_type || "()"}\``);
      lines.push("");
      lines.push("</div>"); // close .api-function
      lines.push("");
      
      // Vitepress slugifies `fn_name` to `fn_name` (underscores preserved).
      // If there are special characters, it strips them. Backticks are stripped.
      const slug = fn.name.toLowerCase().replace(/[^a-z0-9_]+/g, '-').replace(/^-|-$/g, '');
      sidebarItems.push({
        text: fn.name,
        link: `/api/rhai/index#${slug}`
      });
    }
    
    sidebarModules.push({
      text: `${moduleName} module`,
      collapsed: false,
      items: sidebarItems
    });
    
    lines.push("</div>"); // close .api-module
    lines.push("");
  }

  writeFileSync(join(apiRoot, "index.md"), lines.join("\n"), "utf-8");
  
  // Write out the sidebar JSON for the left navigation
  const sidebarOut = resolve(vitepressDir, "api-sidebar.json");
  writeFileSync(sidebarOut, JSON.stringify(sidebarModules, null, 2), "utf-8");

  console.log(
    `[generate-api-docs] Generated single API page with ${Object.keys(modules).length} module(s) and ${fnTotal} function(s).`
  );
}

main();
